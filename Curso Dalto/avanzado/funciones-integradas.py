# Encontrando el numero mayor de una lista, tupla o conjunto 

numeros = [4,3,7,1,42,15]
numero_mas_alto = max(numeros)
print(numero_mas_alto)

numer_mas_bajo = min(numeros)
print(numer_mas_bajo)

# Redondeando a 6 decimales

cifra = 12.545662
cifra_redondeada = round(cifra,2)
print(cifra_redondeada)
# El segundo atributo dentro del parentesis indica cuantos decimales queremos que tenga la variable

